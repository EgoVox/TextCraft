  protected

  # Permettre la mise à jour de l'utilisateur sans fournir le mot de passe
  def update_resource(resource, params)
    if params[:password].blank? && params[:password_confirmation].blank?
      resource.update_without_password(params.except(:current_password))
    else
      super
    end
  end

  def configure_permitted_parameters
    # Pour l'inscription (sign_up)
    devise_parameter_sanitizer.permit(:sign_up, keys: [:first_name, :last_name, :username, :city, :gender, :bio, :email, :password, :password_confirmation, :dark_mode])

    # Pour la mise à jour du compte (account_update)
    devise_parameter_sanitizer.permit(:account_update, keys: [:first_name, :last_name, :username, :city, :gender, :bio, :email, :password, :password_confirmation, :dark_mode])
  end
